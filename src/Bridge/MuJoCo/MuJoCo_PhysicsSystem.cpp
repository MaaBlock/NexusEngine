#include "MuJoCo_PhysicsSystem.h"
#include "Log.h"
#include "ResourceLoader.h"

namespace Nexus {

MuJoCo_PhysicsSystem::MuJoCo_PhysicsSystem() {}

MuJoCo_PhysicsSystem::~MuJoCo_PhysicsSystem() {
    shutdown();
}

Status MuJoCo_PhysicsSystem::initialize() {
    char error[1000] = "Could not load binary model";
    
    // For now, use the hardcoded test scene path. In a real scenario, this would be passed in.
    std::string relativePath = "Data/Scenes/test_scene.xml";
    std::string fullPath = ResourceLoader::getBasePath() + relativePath;
    
    m_model = mj_loadXML(fullPath.c_str(), nullptr, error, 1000);
    if (!m_model) {
        NX_CORE_ERROR("Failed to load MuJoCo model: {} (Error: {})", fullPath, error);
        return InternalError(std::string("Failed to load MuJoCo model: ") + error);
    }

    m_data = mj_makeData(m_model);
    
    NX_CORE_INFO("MuJoCo Physics System Initialized with model: {}", fullPath);
    return OkStatus();
}

void MuJoCo_PhysicsSystem::shutdown() {
    if (m_data) {
        mj_deleteData(m_data);
        m_data = nullptr;
    }
    if (m_model) {
        mj_deleteModel(m_model);
        m_model = nullptr;
    }
    
    NX_CORE_INFO("MuJoCo Physics System Shutdown");
}

void MuJoCo_PhysicsSystem::update(float deltaTime) {
    if (m_model && m_data) {
        mj_step(m_model, m_data);
    }
}

} // namespace Nexus
